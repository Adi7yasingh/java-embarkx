public class RelationalOperators13 {
    public static void main(String[] args) {
         /*

        Relational operators
        == != > < >= <=

         */
        int a = 10, b = 20;
        System.out.println("a==b  : " + (a==b));
        System.out.println("a!=b  : " + (a!=b));
        System.out.println("a>b  : " + (a>b));
        System.out.println("a<b  : " + (a<b));
        System.out.println("a>=b  : " + (a>=b));
        System.out.println("a<=b  : " + (a<=b));

    }
}
