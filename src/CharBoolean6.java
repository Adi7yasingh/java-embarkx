public class CharBoolean6 {
    public static void main(String[] args) {

        // memory size is 1bit
        boolean myBoolean = true;
        System.out.println("myBoolean- " + myBoolean);

        //32bits or 8bytes
        char myChar = 'A';
        System.out.println("char is- " + myChar);

        //char is also used to print unicodes(search on google)
        char unicode = '\u00A9';
        System.out.println("copyright- " + unicode);
    }
}
